package com.learning.es.bean;

public class HighWeight {
    /**
     * 对应字符串
     */
    private String highString;
    /**
     * 比重
     */
    private int weight;

    public HighWeight() {
    }

    public String getHighString() {
        return this.highString;
    }

    public void setHighString(String highString) {
        this.highString = highString;
    }

    public int getWeight() {
        return this.weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
